package _160_Math_Functions;

public class Min_Value_Max_Value {

	public static void main(String[] args) {

		// INTEGER
		System.out.println("\n");
		System.out.println("Integer value ");
		System.out.print(Math.abs(Integer.MIN_VALUE));
		System.out.print(" to ");
		System.out.print(Math.abs(Integer.MAX_VALUE));

		// LONG
		System.out.println("\n");
		System.out.println("Double value ");
		System.out.print(Math.abs(Long.MIN_VALUE));
		System.out.print(" to ");
		System.out.print(Math.abs(Long.MAX_VALUE));

	}
}
