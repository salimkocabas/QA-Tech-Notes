package _001_Basic;

public class _010_Math_2 {
	public static void main(String[] args) {

		int a = 10;
		int b = 10;

		a /= 5; // a=a/5; 
		System.out.println(a);

		b *= 5; // b=b*5
		System.out.println(b);

		System.out.println();
		int c = 10;
		++c;
		System.out.println(c); // c=c+1
		
		c++;
		System.out.println(c); // c=c+1
		
		--c;
		System.out.println(c); // c=c-1
		
		c--;
		System.out.println(c); // c=c-1

	}
}
