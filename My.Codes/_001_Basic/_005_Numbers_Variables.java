package _001_Basic;

public class _005_Numbers_Variables {
	public static void main(String args[]) {
		int num;
		double bignumber;
		float b;
		char c;
		String s;

		// integer
		num = 100;
		// float
		b = 1.2386868684F;
		// character
		c = 'A';
		// string
		s = "Hello Java";
		bignumber = 1234569;

		System.out.println("Value of num			: " + num);
		System.out.println("Value of b			: " + b);
		System.out.println("Value of c			: " + c);
		System.out.println("Value of s			: " + s);
		System.out.println("Value of bignumber		: " + bignumber);
		System.out.println("");
		System.out.println("Differents value has been print on screen");

	}

}
